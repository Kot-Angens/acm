const double EPS = 1e-9;

void solve(istream& is, ostream& os) {
  int n;
  is >> n;
  vector<int> g(n);
  for (int i = 0; i < n; ++i) is >> g[i];
  vector<int> ans(2, 0);
  int side = 0;
  int i = 0, j = n - 1;
  while (i <= j) {
    int v = 0;
    if (g[i] >= g[j]) {
      ans[side] += g[i];
      i += 1;
    } else {
      ans[side] += g[j];
      j -= 1;
    }
    side = (side + 1) & 1;
  }
  os << ans[0] << ":" << ans[1] << endl;
}

const char* liner = "--------";
int problem;
int problem_filter;

