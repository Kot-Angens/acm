const double EPS = 1e-9;

void solve(istream& is, ostream& os) {
  char e[] = "2.7182818284590452353602875";
  int n;
  is >> n;
  if (n == 0) {
    os << 3 << endl;
    return;
  }
  if (e[n + 2] >= '5')
    e[n + 1] += 1;
  e[n + 2] = 0;
  os << e << endl;
}

const char* liner = "--------";
